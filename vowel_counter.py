# Функция подсчета количества русских гласных букв в строке
def count_russian_vowels(text: str) -> int:
    # Строка, содержащая все русские гласные (в нижнем и верхнем регистре)
    vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯ"

    # Суммируем 1 за каждую букву в тексте, если она является гласной
    return sum(1 for char in text if char in vowels)
